/**
 * op_add - calculate the sum of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculates the difference of two integers
 * @a: integer that another integer is substracted from
 * @b: integer to be subtracted
 *
 * Return: difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculates the product of two integers
 * @a: the first integer
 * @b: the second integet
 *
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div -calculates the division of two numbers
 * @a: the integer to be divided
 * @b: the dividing integer
 *
 * Return: quotient of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculates the remender of the division of teo integers
 * @a: the integer to be divided
 * @b: the dividing integer
 *
 * Return: remainder of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
